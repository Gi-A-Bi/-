-- =============================================================================
-- 스킬 보상 사용 횟수 (unlock_uses)
--   - 0  = 계속 유지(상시 스킬, 소모되지 않음)
--   - 1~3 = N회권 (사용할 때마다 차감, 0이 되면 사라짐)
--   - 자동해금 스킬과 A/B 선택형 보상 모두에 적용
--   - 기존 스킬은 기본값 1(1회권)로 시작 (기존 동작과 동일)
--   - Supabase SQL Editor에서 한 번 실행 (여러 번 실행해도 안전)
-- =============================================================================
ALTER TABLE levels
  ADD COLUMN IF NOT EXISTS unlock_uses integer NOT NULL DEFAULT 1;
